-- Fix function search path security issues only
-- Update all user-defined functions to use SECURITY DEFINER and set search_path

-- Update update_content_analysis_updated_at function
CREATE OR REPLACE FUNCTION public.update_content_analysis_updated_at()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$function$;

